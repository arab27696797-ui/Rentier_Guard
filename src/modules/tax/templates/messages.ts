/**
 * Получить ставку патента для города
 */
export function getPatentRate(city: string): number {
  return PATENT_BASE_RATES[city] || 20000; // Значение по умолчанию
}

/**
 * Рассчитать налог для разных режимов
 */
export function calculateTaxes(income: number, city: string): {
  ndfl: number;
  selfEmployed: number;
  patent: number;
} {
  // НДФЛ 13%
  const ndfl = Math.round(income * 0.13);
  
  // Самозанятый 4% с вычетом 10000
  const selfEmployedRaw = income * 0.04;
  const selfEmployed = Math.max(0, Math.round(selfEmployedRaw - 10000));
  
  // Патент (фиксированная ставка)
  const patent = getPatentRate(city);
  
  return { ndfl, selfEmployed, patent };
}

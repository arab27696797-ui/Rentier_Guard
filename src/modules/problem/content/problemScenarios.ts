export const problemScenarios: Record<ProblemType, ProblemScenario> = {
  [ProblemType.NON_PAYMENT]: nonPaymentScenario,
  [ProblemType.PROPERTY_DAMAGE]: damageScenario,
  [ProblemType.EVICTION_WITHOUT_PAY]: evictionScenario,
  [ProblemType.OTHER]: otherProblemScenario,
};

/**
 * Получить сценарий по типу проблемы
 */
export function getScenarioByType(type: ProblemType): ProblemScenario {
  return problemScenarios[type] || otherProblemScenario;
}

/**
 * Получить все сценарии
 */
export function getAllScenarios(): ProblemScenario[] {
  return Object.values(problemScenarios);
}

/**
 * Форматировать дату для документа
 */
function formatDate(date: Date = new Date()): string {
  return date.toLocaleDateString('ru-RU', {
    day: '2-digit',
    month: '2-digit',
    year: 'numeric',
  });
}

/**
 * Форматировать сумму в рублях
 */
function formatAmount(amount: number): string {
  return amount.toLocaleString('ru-RU', {
    style: 'currency',
    currency: 'RUB',
    minimumFractionDigits: 2,
  });
}

/**
 * Форматировать сумму прописью (упрощённо)
 */
function amountToWords(amount: number): string {
  const rubles = Math.floor(amount);
  const kopecks = Math.round((amount - rubles) * 100);
  return `${rubles} (${rubles.toString()}) рублей ${kopecks.toString().padStart(2, '0')} копеек`;
}

/**
 * Получить текущую дату в формате для документа
 */
function getCurrentDate(): string {
  const date = new Date();
  const months = [
    'января', 'февраля', 'марта', 'апреля', 'мая', 'июня',
    'июля', 'августа', 'сентября', 'октября', 'ноября', 'декабря'
  ];
  return `${date.getDate()} ${months[date.getMonth()]} ${date.getFullYear()} г.`;
}


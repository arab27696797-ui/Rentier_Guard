/**
 * Сгенерировать претензию на основе типа и данных
 */
export function generateClaim(
  type: ClaimTemplateType,
  data: any
): ClaimGenerationResult {
  try {
    let claim: GeneratedClaim;

    switch (type) {
      case ClaimTemplateType.NON_PAYMENT:
        claim = generateNonPaymentClaim(data as NonPaymentData);
        break;
      case ClaimTemplateType.DAMAGE:
        claim = generateDamageClaim(data as DamageData);
        break;
      case ClaimTemplateType.EVICTION:
        claim = generateEvictionClaim(data as EvictionData);
        break;
      case ClaimTemplateType.TERMINATION:
        claim = generateTerminationNotice(data as ProblemData & { reason: string });
        break;
      default:
        return {
          success: false,
          error: 'Неизвестный тип претензии',
        };
    }

    return {
      success: true,
      claim,
    };
  } catch (error) {
    return {
      success: false,
      error: error instanceof Error ? error.message : 'Ошибка генерации претензии',
    };
  }
}


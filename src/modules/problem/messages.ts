/**
 * Получить сообщение о типе проблемы
 */
export function getProblemTypeMessage(type: ProblemType): string {
  const messages: Record<ProblemType, string> = {
    [ProblemType.NON_PAYMENT]: '💰 Неуплата аренды',
    [ProblemType.PROPERTY_DAMAGE]: '🔨 Повреждение имущества',
    [ProblemType.EVICTION_WITHOUT_PAY]: '🚪 Выезд без оплаты',
    [ProblemType.OTHER]: '❓ Другая проблема',
  };
  return messages[type];
}

/**
 * Получить emoji для типа проблемы
 */
export function getProblemTypeEmoji(type: ProblemType): string {
  const emojis: Record<ProblemType, string> = {
    [ProblemType.NON_PAYMENT]: '💰',
    [ProblemType.PROPERTY_DAMAGE]: '🔨',
    [ProblemType.EVICTION_WITHOUT_PAY]: '🚪',
    [ProblemType.OTHER]: '❓',
  };
  return emojis[type];
}

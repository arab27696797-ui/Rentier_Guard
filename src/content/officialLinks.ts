/**
 * Получить ссылку по ключу
 */
export function getLink(category: string, key: string): string | undefined {
  const categoryData = ALL_OFFICIAL_LINKS[category as keyof typeof ALL_OFFICIAL_LINKS];
  if (categoryData && typeof categoryData === 'object') {
    const linkData = categoryData[key as keyof typeof categoryData];
    if (linkData && typeof linkData === 'object' && 'url' in linkData) {
      return linkData.url;
    }
  }
  return undefined;
}

/**
 * Получить все ссылки категории
 */
export function getCategoryLinks(category: string) {
  return ALL_OFFICIAL_LINKS[category as keyof typeof ALL_OFFICIAL_LINKS];
}

